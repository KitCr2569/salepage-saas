const fs = require('fs');

function patchFile(path, findStr, replaceStr, logName) {
    let content = fs.readFileSync(path, 'utf8');
    if (content.includes(findStr)) {
        content = content.replace(findStr, replaceStr);
        fs.writeFileSync(path, content);
        console.log("✅ Patched " + logName);
    } else {
        console.log("⚠️ Could not find target string in " + logName);
    }
}

